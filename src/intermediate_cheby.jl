@inline function _intermediate_small_base(v, x)
  u = v*v
  z = log(x)
  sx = muladd(u, _INTERMEDIATE_SMALL_MX, -_INTERMEDIATE_SMALL_CX)
  sy = muladd(z, _INTERMEDIATE_SMALL_MY, -_INTERMEDIATE_SMALL_CY)
  r = _cheby2d(sx, sy, _INTERMEDIATE_SMALL_COEFS)
  SQRT_PID2*inv(sqrt(x))*r
end

@inline function _intermediate_large_base(v, x)
  u = v*v
  w = inv(x)
  sx = muladd(u, _INTERMEDIATE_LARGE_MX, -_INTERMEDIATE_LARGE_CX)
  sy = muladd(w, _INTERMEDIATE_LARGE_MY, -_INTERMEDIATE_LARGE_CY)
  r = _cheby2d(sx, sy, _INTERMEDIATE_LARGE_COEFS)
  SQRT_PID2*inv(sqrt(x))*r
end

@inline function _intermediate_pair_orders(v, delta)
  mu = abs(v)

  if _primal(mu) <= 1.0+delta
    return (mu, abs(mu-1.0), mu, 0)
  end

  n = floor(Int, _primal(mu))
  alpha = mu-n
  (alpha, abs(alpha-1.0), alpha, n)
end

@inline function _intermediate_forward_recurrence(kprev, kcur, alpha, niter, x)
  km1 = kprev
  k0 = kcur

  for j in 0:(niter-1)
    nu = alpha+j
    kp1 = km1+(2nu/x)*k0
    km1 = k0
    k0 = kp1
  end

  k0
end

@inline function _intermediate_besselkx(v, x, delta, base)
  p = _intermediate_pair_orders(v, delta)
  kcur = base(p[1], x)

  p[4] == 0 && return kcur

  kprev = base(p[2], x)
  _intermediate_forward_recurrence(kprev, kcur, p[3], p[4], x)
end

@inline function _intermediate_besselkx(v, x)
  if _primal(x) <= 4.0
    return _intermediate_besselkx(v, x, _INTERMEDIATE_SMALL_DELTA, _intermediate_small_base)
  end
  _intermediate_besselkx(v, x, _INTERMEDIATE_LARGE_DELTA, _intermediate_large_base)
end

@inline function _besselk_intermediate(v, x, modify)
  kx = _intermediate_besselkx(v, x)
  modify ? exp(v*log(x)-x)*kx : exp(-x)*kx
end
